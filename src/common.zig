const builtin = @import("builtin");

pub const InitOptions = struct {
    name: [:0]const u8 = "Window",
    width: u16 = 640,
    height: u16 = 360,

    surface: union(enum) {
        none,
        opengl: struct {
            major: u8,
            minor: u8,

            pub const @"4.5": @This() = .{
                .major = 4,
                .minor = 5,
            };

            pub const @"3.2": @This() = .{
                .major = 3,
                .minor = 2,
            };
        },
    } = .none,
};

